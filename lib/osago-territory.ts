// Источник: Указание Банка России от 09.10.2025 № 7204-У (Приложение 2),
// действует с 09.12.2025. Значения сверены по нескольким открытым источникам;
// т.к. это регулируемые данные, которые периодически пересматриваются ЦБ,
// стоит проверять актуальность раз в год.
//
// Группировка сделана по субъекту РФ (optgroup в select), внутри — по городам.

export type TerritoryOption = { value: string; label: string; coefficient: number };
export type TerritoryGroup = { group: string; options: TerritoryOption[] };

let counter = 0;
function opt(label: string, coefficient: number): TerritoryOption {
  counter += 1;
  return { value: `t${counter}`, label, coefficient };
}

